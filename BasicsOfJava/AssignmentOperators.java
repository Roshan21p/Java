package BasicsOfJava;

public class AssignmentOperators {
    public static void main(String[] args) {
        int p = 10, q;

        // =
        q = p;
        System.out.println(q);//10

        // +=
        p += q ;// p = p +q = 20
        System.out.println(p);

        // -=
        p -= q; // p = p - q = 10
        System.out.println(p);

        // *=
        p *= q; // p = p*q = 100
        System.out.println(p);

        // /=
        p /= q; // p = p / q = 10
        System.out.println(p);


    }
}
